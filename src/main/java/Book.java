public class Book {

    private String ID;
    private String title;
    private int quantity;
    private double price;
    private String type;

    public Book(String ID, String title, int quantity, double price, String type) {
        this.ID = ID;
        this.title = title;
        this.quantity = quantity;
        this.price = price;
        this.type = type;
    }

}
